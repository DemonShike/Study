#operadores aritmeticos (nos permiten hacer operaciones con variables y numeros)
numero1 = 77
numero2 = 44  #operador de asignacion = 

resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2 
resto = numero1 % numero2 #nos da el resto de los numeros elegidos

print("************** CALCULADORA **************")
print(f"La Resta es : {resta}")
print(f"La Suma es : {numero1 + numero2}")
print("La multiplicacicon es: ", multiplicacion) #esta es otra forma de mostrar el resultado que funciona igual que las demas
print("La divsion es: ", division) 
print("El resto de numero 1 y numero 2 es:{}".format(resto))